void UART_LB(void)
{
	uint8_t Buffer[10];
	uint8_t i, m = 0;
	while(1)
	{
		uart_in = UART1_RX();
		if(uart_in == 13)
		{
			break;
		}
		else{
			Buffer[i] = uart_in;
			i++;
		}
	}
	//UART1_TX(0x30);
	
	for(m = 0; m < i; m++)
	{
		UART1_TX(Buffer[m]);
	}
	
}

void ST7735_KeyBoard(uint8_t x, uint8_t y){
	uint8_t IsBackSpace = 0;
	while(1){ 
		IsBackSpace = 0;		
		uart_in = UART1_RX();
		if(uart_in == 13)
		{
			x = 0;
			y = y + 10;
			continue;
		}
		if(uart_in == 8)
		{
			IsBackSpace = 1;
			if(x >= 6){
				x = x - 6;
			}
			else{
				x = 0;
			}
			uart_in = ' ';
		}
		DrawChar(x, y, uart_in, 0, 0xFFFF, 1);  
		if(!IsBackSpace)
		{
			x = x + 6;
		}
		if(x > 120)
		{
			x = 0;
			y = y + 10;
		}
	}
}

void Universal_SetCursor(uint8_t *x, uint8_t *y){

	uint8_t Buffer[3] = {0, 0, 0};
	uint8_t i, m = 0;
	DebugPort("Input X coordinator");
	while(1)
	{		
		uart_in = UART1_RX();
		if(uart_in == 13)
		{
			*x = 0;
			break;
		}
		else{
			Buffer[i] = uart_in - 0x30;
			i++;
		}
	}
	for(m = i; m > 0; m--)
	{
		*x += Buffer[m-1]*pow(10, i - m);
	}

	
	DebugPort("Input Y coordinator");
	i = 0;
	while(1)
	{		
		uart_in = UART1_RX();
		if(uart_in == 13)
		{
			*y = 0;
			break;
		}
		else{
			Buffer[i] = uart_in - 0x30;
			i++;
		}
	}
	for(m = i; m > 0; m--)
	{
		*y += Buffer[m-1]*pow(10, i - m);
	}
}

